package stream;

public class Curso {
	
	String nome;
	int alunos;
	public Curso(String nome, int i) {
		super();
		this.nome = nome;
		this.alunos = i;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getAlunos() {
		return alunos;
	}
	public void setAlunos(int alunos) {
		this.alunos = alunos;
	}

	

}
